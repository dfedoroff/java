package main.database;

/**
 * Перечисление, представляющее состояние сущности в базе данных.
 */
public enum EntityState {
    NEW,        // Новая сущность, которая ещё не сохранена в базе данных
    MODIFIED,   // Сущность была изменена и требует обновления в базе данных
    DELETED,    // Сущность должна быть удалена из базы данных
    UNCHANGED   // Сущность не изменялась с момента последнего чтения из базы данных
}
